                    <br><br>Note que a ênfase do dispositivo constitucional volta-se, claramente, para a definição de obras e equipamentos de caráter <strong>estruturante</strong> ao desenvolvimento local, estadual ou nacional, bem como ao planejamento do conjunto de serviços públicos e ações de caráter <strong>permanente</strong>. Na consideração desses dois aspectos, residem a natureza estratégica e a razão de ser do PPA, qual seja, a realização de ações estruturantes e permanentes para alavancar o desenvolvimento humano, social e econômico.
                    <br><br>A Constituição Federal define, ainda, que todas as despesas do PPA devem ser regionalizadas. No caso da União, a regionalização se dá pelas cinco macrorregiões definidas pelo IBGE – Norte, Nordeste, Sul, Sudeste e Centro-Oeste. Além dessas, é utilizada também a categoria <strong>“Nacional”</strong> para as despesas que não são regionalizadas ou que, por sua natureza, não se prestam à regionalização. No caso dos Estados e municípios, a regionalização se reporta a distritos definidos segundo critérios do governo local (regiões administrativas, distritos sanitários etc.).
                    <br><br>O PPA tem, na concepção do sistema orçamentário brasileiro, a função primordial de orientar a programação de ações da Administração Pública, as quais deverão estar sempre em consonância com aquele. É o que se depreende de vários dispositivos constitucionais.
