                    <br><br>A função do dispositivo constitucional é estabelecer condições que deverão ser observadas pelo PPA. Uma dessas condições é que o PPA especifique as despesas de capital a serem realizadas na vigência do plano e explicite a que objetivos e metas essas despesas estão relacionadas.
                    <br><br>As despesas de capital são aquelas relativas, principalmente, às obras, às instalações, aos equipamentos e a material permanente, ou seja, às despesas que contribuam, diretamente, para a formação ou aquisição de um bem de capital. Temos ainda as despesas decorrentes das despesas de capital que envolvem todos os gastos relacionados à manutenção das obras, às instalações, aos equipamentos e ao custeio de sua utilização e funcionamento. 
